#include <stdio.h>
#include <math.h>

/* Program  to calculate the cube root of a number using cbrt() function */
int main()
{
    int result = cbrt(27); /* The cbrt function should be used with the math.h header file*/
    printf("The cube root of the number is %d\n", result);
    /* Prints the cube root of the number */
    
    return(0);
}
