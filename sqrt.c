#include <stdio.h>
#include <math.h>

/* Program  to calculate the square root of a number using sqrt() function */
int main()
{
    int result = sqrt(25); /* The sqrt function should be used with the math.h header file*/
    printf("The square root of the number is %d\n", result);
    /* Prints the square root of the number */
    
    return(0);
}
